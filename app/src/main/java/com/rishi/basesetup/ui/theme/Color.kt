val isInLightTheme
    @ReadOnlyComposable
    @Composable
    get() = true

val ColorScheme.textFieldStrokeFocused: Color
    @Composable
    get() = if (isInLightTheme) strokeFilledColor else strokeUnfilledColor

val ColorScheme.textFieldStrokeNotFocused: Color
    @Composable
    get() = if (isInLightTheme) strokeUnfilledColor else strokeFilledColor

val ColorScheme.textFieldTextColor: Color
    @Composable
    get() = if (isInLightTheme) textColor else Color.White

val ColorScheme.disabledBgColor: Color
    @Composable
    get() = if (isInLightTheme) disabledBackgroundColor else strokeFilledColor

